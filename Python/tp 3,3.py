'''a) Calcule la cantidad de segundos de un tiempo dado (por parámetros) en horas, minutos y segundos, y devuelva el resultado al programa principal.'''

def segundos(h, m, s):
    sh= h*3600
    sm= m*60
    return sh+sm+s

